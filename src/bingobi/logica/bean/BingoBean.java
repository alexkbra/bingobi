package bingobi.logica.bean;

/**
 *
 * @author kbra
 */
public class BingoBean {
    
    Integer b;
    Integer i;
    Integer n;
    Integer g;
    Integer o;

    public BingoBean(Integer b, Integer i, Integer n, Integer g, Integer o) {
        this.b = b;
        this.i = i;
        this.n = n;
        this.g = g;
        this.o = o;
    }

    public Integer getB() {
        return b;
    }

    public void setB(Integer b) {
        this.b = b;
    }

    public Integer getI() {
        return i;
    }

    public void setI(Integer i) {
        this.i = i;
    }

    public Integer getN() {
        return n;
    }

    public void setN(Integer n) {
        this.n = n;
    }

    public Integer getG() {
        return g;
    }

    public void setG(Integer g) {
        this.g = g;
    }

    public Integer getO() {
        return o;
    }

    public void setO(Integer o) {
        this.o = o;
    }
    
    
    
}
